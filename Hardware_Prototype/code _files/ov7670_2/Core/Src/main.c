void OV7670_Init(void) {
  HAL_GPIO_WritePin(GPIOE, GPIO_PIN_6, GPIO_PIN_RESET);
  HAL_Delay(100);
  HAL_GPIO_WritePin(GPIOE, GPIO_PIN_6, GPIO_PIN_SET);
  HAL_Delay(100);
  
  // Configure OV7670 registers via I2C
  OV7670_WriteReg(0x12, 0x80); // Reset all registers
  HAL_Delay(100);
  
  // Set up QVGA RGB565 output
  OV7670_WriteReg(0x12, 0x0C); // Output format - RGB
  OV7670_WriteReg(0x40, 0xD0); // RGB565
  OV7670_WriteReg(0x3A, 0x04); // TSLB - set RGB565
  OV7670_WriteReg(0x11, 0x80); // PCLK divider
  OV7670_WriteReg(0x0C, 0x08); // COM3 - enable scaling
  OV7670_WriteReg(0x3E, 0x08); // COM14 - scaling parameter
  OV7670_WriteReg(0x72, 0x11); // Scaling X
  OV7670_WriteReg(0x73, 0xF1); // Scaling Y
}

